import 'Rocket.dart';

class Launch{
  final int flightNumber;
  final String missionName;
  final String launchYear;
  final String rocketId;
  final String missionImage;
  final String launchDate;
  Rocket rocket;

  Launch({this.flightNumber,this.missionName,this.launchYear,this.rocketId,this.missionImage,this.rocket,this.launchDate});
}
